-- 4. Automatische Timestamp-Updates
-- ============================================================================
create or replace function public.handle_updated_at()
returns trigger as $$
begin
  new.updated_at = timezone('utc'::text, now());
  return new;
end;
$$ language plpgsql security definer;

-- Trigger fÃ¼r user_cards
create trigger on_user_cards_updated
  before update on public.user_cards
  for each row
  execute procedure public.handle_updated_at();

-- Trigger fÃ¼r profiles
create trigger on_profiles_updated
  before update on public.profiles
  for each row
  execute procedure public.handle_updated_at();


-- 5. Automatische Profil-Erstellung bei Registrierung
-- ============================================================================
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, username)
  values (
    new.id,
    split_part(new.email, '@', 1)  -- Username = Teil vor @
  );
  return new;
end;
$$ language plpgsql security definer;

-- Trigger bei neuem User
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row
  execute procedure public.handle_new_user();


