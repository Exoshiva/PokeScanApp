-- Funktion: Hole Top-Wert-Karten eines Users
create or replace function public.get_top_value_cards(
  p_user_id uuid,
  p_limit int default 10
)
returns table (
  id bigint,
  name text,
  image_url text,
  total_value numeric
) as $$
begin
  return query
  select
    uc.id,
    uc.name,
    uc.image_url,
    (uc.current_market_price_eur * uc.quantity) as total_value
  from public.user_cards uc
  where uc.user_id = p_user_id
    and uc.current_market_price_eur is not null
  order by total_value desc
  limit p_limit;
end;
$$ language plpgsql security definer;

-- Grant Zugriff
grant execute on function public.get_top_value_cards to authenticated;


